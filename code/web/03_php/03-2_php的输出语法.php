<?php
	/*
		php 的输出语法
		 + 后端的输出和前端的输出不一样
		 + 注意：后端的输出，只是单纯的输出，不考虑输出在什么位置
		  => 取决于谁在请求它

		例子：
		 => 后端：输出 123
		 => 当你在 浏览器地址栏 书写该 PHP 文件时，输出就会输出给浏览器
		 => 当你在 ajax 书写该 PHP 文件时，就会输出在 xhr.responseText 上
		 => 当你用 java 去访问 PHP 文件时，就会输出给 java
		 => ...

		 语法：
		  1.echo 输出的内容;
		  2.print_r(输出的内容);
		  3.var_dump(输出的内容);

		  区别：
		   1.echo 只能输出 基本数据类型
		    => 不能准确输出复杂数据类型
			=> 注意：输出布尔 true 会转换成 1，输出布尔 false会转换成 空
		   2.print_r()可以输出所有数据类型
		    => 注意：输出布尔 true 会转换成 1，输出布尔 false会转换成 空
	*/
	// 1.echo	
	echo 'hello world';
	// 2.print_r
	print_r('你好 世界');			
	// 3.var_dump
	var_dump('你好 世界');
?>